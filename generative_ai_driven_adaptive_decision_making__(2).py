import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.preprocessing import StandardScaler, LabelEncoder
from tensorflow.keras.models import Model
from tensorflow.keras.layers import Input, Dense, Concatenate, LeakyReLU
from tensorflow.keras.optimizers import Adam
from tensorflow.keras import Sequential
from tensorflow.keras.utils import to_categorical

import tensorflow as tf
tf.config.run_functions_eagerly(True)


# Preprocess
#df = df.drop(columns=['Timestamp'])
df['Operation_Mode'] = LabelEncoder().fit_transform(df['Operation_Mode'])
df['Efficiency_Status'] = LabelEncoder().fit_transform(df['Efficiency_Status'])

# Sensor features
sensor_cols = ['Temperature_C', 'Vibration_Hz', 'Power_Consumption_kW',
               'Network_Latency_ms', 'Packet_Loss_%', 'Production_Speed_units_per_hr']
scaler = StandardScaler()
sensor_data = scaler.fit_transform(df[sensor_cols])

# Condition inputs: Operation_Mode + Efficiency_Status
conditions = df[['Operation_Mode', 'Efficiency_Status']].values
conditions = to_categorical(conditions[:, 0]*3 + conditions[:, 1], num_classes=9)  # 3 modes × 3 status

# GAN settings
latent_dim = 32
condition_dim = conditions.shape[1]
sensor_dim = sensor_data.shape[1]

# Generator
def build_generator():
    model = Sequential()
    model.add(Dense(64, input_dim=latent_dim + condition_dim))
    model.add(LeakyReLU(0.2))
    model.add(Dense(128))
    model.add(LeakyReLU(0.2))
    model.add(Dense(sensor_dim, activation='linear'))
    return model

# Discriminator
def build_discriminator():
    model = Sequential()
    model.add(Dense(64, input_dim=sensor_dim + condition_dim))
    model.add(LeakyReLU(0.2))
    model.add(Dense(32))
    model.add(LeakyReLU(0.2))
    model.add(Dense(1, activation='sigmoid'))
    return model

# Use separate optimizers
optimizer_d = Adam(0.0002, 0.5)
optimizer_g = Adam(0.0002, 0.5)

# Build models
generator = build_generator()
discriminator = build_discriminator()
discriminator.compile(loss='binary_crossentropy', optimizer=optimizer_d, metrics=['accuracy'])

# Combined model
z = Input(shape=(latent_dim,))
c = Input(shape=(condition_dim,))
zc = Concatenate()([z, c])
fake_sensor = generator(zc)
dc_input = Concatenate()([fake_sensor, c])
validity = discriminator(dc_input)

combined = Model([z, c], validity)
combined.compile(loss='binary_crossentropy', optimizer=optimizer_g)


# Training loop
epochs = 5000
batch_size = 64
half_batch = batch_size // 2

for epoch in range(epochs):
    idx = np.random.randint(0, sensor_data.shape[0], half_batch)
    real_sensor = sensor_data[idx]
    real_cond = conditions[idx]

    z_noise = np.random.normal(0, 1, (half_batch, latent_dim))
    gen_sensor = generator.predict(np.concatenate([z_noise, real_cond], axis=1))

    # Train discriminator
    d_loss_real = discriminator.train_on_batch(np.concatenate([real_sensor, real_cond], axis=1), np.ones((half_batch, 1)))
    d_loss_fake = discriminator.train_on_batch(np.concatenate([gen_sensor, real_cond], axis=1), np.zeros((half_batch, 1)))
    d_loss = 0.5 * np.add(d_loss_real, d_loss_fake)

    # Train generator
    z_noise = np.random.normal(0, 1, (batch_size, latent_dim))
    sampled_cond = conditions[np.random.randint(0, conditions.shape[0], batch_size)]
    g_loss = combined.train_on_batch([z_noise, sampled_cond], np.ones((batch_size, 1)))

    # Print progress
    if epoch % 500 == 0:
        print(f"Epoch {epoch} | D Loss: {d_loss[0]:.4f}, Acc: {100*d_loss[1]:.2f}% | G Loss: {g_loss:.4f}")

# Generate new sensor samples
n_samples = 100
z_gen = np.random.normal(0, 1, (n_samples, latent_dim))
target_cond = np.tile(to_categorical([2*3 + 2], 9), (n_samples, 1))  # Mode=2, Efficiency=2
generated = generator.predict(np.concatenate([z_gen, target_cond], axis=1))
generated = scaler.inverse_transform(generated)

# Plot generated sensor curves
plt.figure(figsize=(10, 5))
